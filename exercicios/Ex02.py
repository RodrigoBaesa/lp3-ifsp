# Ex02 - Escreva um programa em Python que solicita ao usuário três números e apresenta a média aritmética dos números informados.

n1 = float(input("Insira sua primeira nota: "))
n2 = float(input("Insira sua segunda nota: "))
n3 = float(input("Insira sua primeira nota: "))

media = (n1 + n2 + n3) / 3

print(f"Sua média é: {media}")